const server = http.createServer(async (req, res) => {
  try {
    const urlPath = decodeURIComponent(req.url.split('?')[0]);

    if (req.method === 'GET' && (urlPath === '/' || urlPath === '')) {
      res.writeHead(200, {'Content-Type':'text/html; charset=utf-8'});
      res.end(INDEX_HTML);
      return;
    }

    // Serve generated files under /generated
    if (req.method === 'GET' && urlPath.startsWith('/generated/')) {
      const filePath = safeJoin(root, urlPath);
      if (!filePath) { res.writeHead(403); res.end('Forbidden'); return; }
      try { await fs.promises.access(filePath); sendFile(filePath, res); } catch { res.writeHead(404); res.end('Not found'); }
      return;
    }

    // avatar upload endpoint removed

    // Trigger generation
    if (req.method === 'POST' && urlPath === '/api/generate') {
      let body=''; for await (const chunk of req) body+=chunk;
      try {
        const cfg = JSON.parse(body);

        // Build environment for child process
        const env = Object.assign({}, process.env);
        if (cfg.font) env.REPORT_FONT = cfg.font;
        if (cfg.bg) env.REPORT_BG = cfg.bg;
        if (cfg.accent) env.REPORT_ACCENT = cfg.accent;
        if (cfg.footer) env.REPORT_FOOTER_TEXT = cfg.footer;
        if (cfg.from) env.REPORT_DATE_FROM = cfg.from;
        if (cfg.to) env.REPORT_DATE_TO = cfg.to;

        // Spawn index.js with --force to produce fresh output
        const child = spawn(process.execPath, [path.join(root,'index.js'), '--force'], { env, cwd: root });
        let stdout = '', stderr = '';
        child.stdout.on('data', d => { stdout += d.toString(); });
        child.stderr.on('data', d => { stderr += d.toString(); });
        child.on('close', code => {
          if (code === 0) {
            res.writeHead(200, {'Content-Type':'application/json'});
            res.end(JSON.stringify({ok:true}));
          } else {
            res.writeHead(500, {'Content-Type':'application/json'});
            res.end(JSON.stringify({ok:false,error:stderr||stdout||('exit '+code)}));
          }
        });
      } catch (e) { res.writeHead(500,{'Content-Type':'application/json'}); res.end(JSON.stringify({ok:false,error:e.message})); }
      return;
    }

    // Fallback: not found
    res.writeHead(404);
    res.end('Not found');
  } catch (err) {
    res.writeHead(500);
    res.end('Server error');
  }
});

server.listen(PORT, () => console.log(`UI server running: http://localhost:${PORT}/`));
