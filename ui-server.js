import http from 'http';
import fs from 'fs';
import path from 'path';
import { spawn } from 'child_process';

const PORT = process.env.PORT || 5174;
const root = path.resolve('.');
const GENERATED_DIR = path.join(root, 'generated');
const GENERATED_ASSETS_DIR = path.join(GENERATED_DIR, 'assets');

const mime = {
  '.html': 'text/html; charset=utf-8',
  '.css': 'text/css; charset=utf-8',
  '.js': 'application/javascript; charset=utf-8',
  '.json': 'application/json; charset=utf-8',
  '.png': 'image/png',
  '.jpg': 'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.svg': 'image/svg+xml',
  '.ico': 'image/x-icon',
  '.webp': 'image/webp',
  '.gif': 'image/gif',
};

function safeJoin(rootDir, urlPath) {
  const normalized = path.normalize(path.join(rootDir, urlPath));
  if (!normalized.startsWith(rootDir)) return null;
  return normalized;
}

function sendFile(fp, res) {
  const ext = path.extname(fp).toLowerCase();
  const ct = mime[ext] || 'application/octet-stream';
  res.writeHead(200, { 'Content-Type': ct, 'Cache-Control': 'no-cache' });
  const stream = fs.createReadStream(fp);
  stream.pipe(res);
  stream.on('error', () => { res.writeHead(500); res.end('Read error'); });
}

// Ensure generated assets dir exists
fs.mkdirSync(GENERATED_ASSETS_DIR, { recursive: true });

